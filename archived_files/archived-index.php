<p>
<strong>Voices for Earth Justice is an interfaith network of people committed to spiritual development, education and actions that deepen our sense of wonder, responsibility, and gratitude for all creation.</strong></p>

<p>We serve people and groups who desire a deeper connection with all creation and each other.</p>
<p>We draw energy from our diverse spiritualities and the wisdom of our faith traditions to better care for Creation.</p>

<p>We work in collaboration with many faith and non-faith based organizations. Much of our current efforts are being focused in Detroit in the Brightmoor neighborhood, where we work with our neighbors in gardening and promotion of sustainable use of land, soil, and water.</p>

<p><strong>We Provide ...</strong></p>

<ul>
	<li>Programs like: Connecting with Creation, Awakening the Dreamer, Generation Waking Up and women's retreats</li>
